liste = circuit.split()

converter = {
    "0": 0,
    "-": 1,
    "D": 2,
    "A": 3,
    "/": 4,
    "\\": 5,
    "|": 6,
    "_": 7,
    "‾": 8
}

chars = [[] for i in range(len(liste))]
print("let circuit = \n[|")
for i in range(len(chars)):
    formated = ""
    print("  [|", end="")
    temp = list(liste[i])
    for char in temp:
        formated += f"{converter[char]};"
    print(formated, end="")
    print("|];")
print("|]")

